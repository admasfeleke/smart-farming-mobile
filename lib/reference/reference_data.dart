  static List<Map<String, dynamic>> mergeByIdThenName(
    List<Map<String, dynamic>> primary,
    List<Map<String, dynamic>> fallback,
  ) {
    final byKey = <String, Map<String, dynamic>>{};
    for (final item in fallback) {
      byKey[_key(item)] = Map<String, dynamic>.from(item);
    }
    for (final item in primary) {
      byKey[_key(item)] = Map<String, dynamic>.from(item);
    }
    final values = byKey.values.toList(growable: false);
    values.sort((a, b) => (a['name']?.toString() ?? '').compareTo(b['name']?.toString() ?? ''));
    return values;
  }

  static String _key(Map<String, dynamic> item) {
    final id = item['id'];
    if (id != null) return 'id:$id';
    return 'name:${item['name']?.toString().trim().toLowerCase() ?? ''}';
  }
