  static String t(
    String lang,
    String key, {
    Map<String, String> params = const {},
  }) {
    final normalizedLang = LanguageConfig.normalize(lang);
    final localized = _strings[normalizedLang]?[key];
    final amharic = _strings['am']?[key];
    final english = _strings['en']?[key];

    // Never borrow another local language as a silent fallback. It is less
    // confusing to show the canonical English fallback than to mix Amharic,
    // Afaan Oromo, and Tigrinya on the same screen.
    final template = localized ?? english ?? amharic ?? key;
    if (params.isEmpty) return template;
    var result = template;
    params.forEach((k, v) {
      result = result.replaceAll('{$k}', v);
    });
    return result;
  }
